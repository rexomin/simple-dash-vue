<template>
	<div class="sd-input-wrapper">
		<label v-if="label != '' && id != ''" :for="id">{{label}}</label>
		<textarea v-if="id != ''" :id="id" :type="type" :placeholder="placeholder" :value="value" @input="handleInput"></textarea>
	</div>
</template>

<script>
export default {
	props: {
		type: {
			default: "text"
		},
		placeholder: {
			default: ""
		},
		label: {
			default: ""
		},
		id: {
			required: true,
			default: "",
			type: String
		},
		value: {
			default: ""
		},
	},
	methods: {
		handleInput(e) {
			this.$emit('input', e.target.value);
		}
	}
}
</script>

